  def part_two do
    # input = input2(:test)
    input = input2(:real)
    {words, phrases} = parse(input)
    words = words ++ Enum.map(words, &String.reverse/1)
    text = Enum.join(phrases, "     ")

    words
    |> Enum.map(fn w -> scan_word(w, byte_size(w), text, 0, []) end)
    |> :lists.flatten()
    |> Enum.sort()
    |> Enum.map(fn {i, len} -> i..(i + len)//1 end)
    |> Enum.reduce([], fn matched, acc -> insert(acc, matched) end)
    |> Enum.map(fn s..e//1 -> e - s end)
    |> Enum.sum()
  end

  defp scan_word(_w, _size, "", _offset, acc) do
    acc
  end

  defp scan_word(w, size, text, offset, acc) do
    acc =
      case text do
        <<^w::binary, _::binary>> -> [{offset, size} | acc]
        _ -> acc
      end

    <<_::utf8, rest::binary>> = text

    scan_word(w, size, rest, offset + 1, acc)
  end

  defp insert([h | t], se) do
    if Range.disjoint?(h, se) do
      [h | insert(t, se)]
    else
      insert(t, merge_range(h, se))
    end
  end

  defp insert([], se), do: [se]

  defp merge_range(a..b//1, x..y//1) do
    min(a, x)..max(b, y)//1
  end

