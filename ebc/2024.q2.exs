  def part_three do
    # input = input3(:test)
    input = input3(:real)
    {words, [phrase1 | _] = phrases} = parse(input)

    width = String.length(phrase1)
    true = Enum.all?(phrases, &(String.length(&1) == width))

    words = Enum.uniq(words)

    base_grid = AdventOfCode.Grid.parse_lines(phrases, &{:ok, <<&1>>})
    left_grid = Map.new(base_grid, fn {xy, c} -> {AdventOfCode.Grid.translate(xy, :w, width), c} end)
    right_grid = Map.new(base_grid, fn {xy, c} -> {AdventOfCode.Grid.translate(xy, :e, width), c} end)

    full_grid = Map.merge(Map.merge(base_grid, left_grid), right_grid)

    raw_matches =
      for pos <- :lists.reverse(Enum.sort(Map.keys(base_grid))), word <- words do
        # IO.puts("#{inspect(pos)}: #{word}")
        match_pos(pos, word, full_grid)
      end

    raw_matches = :lists.flatten(raw_matches)

    raw_matches
    |> Enum.map(&reproject(&1, width))
    |> Enum.uniq()
    |> Enum.count()
  end

  defp match_pos(pos, word, full_grid) do
    graphemes = String.graphemes(word)
    left_to_right = Enum.zip(left_to_right_poses(pos, word), graphemes)
    right_to_left = Enum.zip(right_to_left_poses(pos, word), graphemes)
    top_to_bottom = Enum.zip(top_to_bottom_poses(pos, word), graphemes)
    bottom_to_top = Enum.zip(bottom_to_top_poses(pos, word), graphemes)

    [
      keys_if_match_all(left_to_right, full_grid),
      keys_if_match_all(right_to_left, full_grid),
      keys_if_match_all(top_to_bottom, full_grid),
      keys_if_match_all(bottom_to_top, full_grid)
    ]
  end

  defp left_to_right_poses(start, word), do: coord_stream(start, word, :e)
  defp right_to_left_poses(start, word), do: coord_stream(start, word, :w)
  defp top_to_bottom_poses(start, word), do: coord_stream(start, word, :s)
  defp bottom_to_top_poses(start, word), do: coord_stream(start, word, :n)

  # matching pattern for 1-char word

  defp coord_stream(start, <<_>>, _direction) do
    [start]
  end

  # Repeat matching patterns for each possible word size
  #
  #    defp coord_stream(start, <<a, b>>, direction) do
  #      [start, AdventOfCode.Grid.translate(start, direction)]
  #    end
  #
  #    defp coord_stream(start, <<a, b, c>>, direction) do
  #      [start, AdventOfCode.Grid.translate(start, direction), AdventOfCode.Grid.translate(start, direction, 2)]
  #    end
  #
  #
  # So we do not have to rely on slow Stream.iterate:
  #
  #   defp coord_stream(start, _word, direction) do
  #     Stream.iterate(start, &AdventOfCode.Grid.translate(&1, direction))
  #   end
  #

  Enum.each(2..10, fn numchars ->
    bin_splats = List.duplicate(quote(do: _), numchars)

    trans_splats =
      Enum.map(1..numchars, fn n ->
        quote do
          AdventOfCode.Grid.translate(var!(start), var!(direction), unquote(n))
        end
      end)

    defp coord_stream(start, <<unquote_splicing(bin_splats)>>, direction) do
      [start, unquote_splicing(trans_splats)]
    end
  end)

  defp keys_if_match_all(symbols, full_grid) do
    if Enum.all?(symbols, fn {{_, _} = xy, c} -> Map.get(full_grid, xy) == c end) do
      Enum.map(symbols, &elem(&1, 0))
    else
      []
    end
  end

  defp reproject({x, y}, width) when x < 0, do: {x + width, y}
  defp reproject({x, y}, width) when x >= width, do: {x - width, y}
  defp reproject({x, y}, _), do: {x, y}
end

